# range() ფუნქცია გამოიყენება რიცხვების მიმდევრობის (დიაპაზონის) შესაქმნელად.
# ის ძირითადად ციკლებთან ერთად გამოიყენება.

# range() - შეიძლება გადაეცეს 1, 2 ან 3 რიცხვი.
# მათი მნიშვნელობები ასეთია:
# 1️⃣ range(stop) - თუ მხოლოდ ერთი რიცხვი გადავეცით, ის იქნება საბოლოო მნიშვნელობა (stop), ხოლო საწყისი მნიშვნელობა 0 იქნება.
# 2️⃣ range(start, stop) - თუ ორი რიცხვი გადავეცით, პირველი იქნება საწყისი (start), მეორე კი საბოლოო (stop).
# 3️⃣ range(start, stop, step) - თუ სამი რიცხვი გადავეცით, პირველი იქნება საწყისი, მეორე - საბოლოო, ხოლო მესამე - ბიჯი (step).

# 1️⃣ ერთი რიცხვით გამოყენება
print("Example 1:")
for i in range(5):  # ამ შემთხვევაში, start ავტომატურად იქნება 0, stop = 5, ხოლო step = 1
    print(i)  # 0, 1, 2, 3, 4

# 2️⃣ ორი რიცხვით გამოყენება
print("\nExample 2:")
for i in range(2, 6):  # start = 2, stop = 6, step = 1
    print(i)  # 2, 3, 4, 5

# 3️⃣ სამი რიცხვით გამოყენება
print("\nExample 3:")
for i in range(1, 10, 2):  # start = 1, stop = 10, step = 2
    print(i)  # 1, 3, 5, 7, 9

# თუ არ გადავცემთ 3 რიცხვს, ავტომატურად მიღებული იქნება:
# range(n) -> start=0, step=1
# range(a, b) -> step=1
# ხოლო range(a, b, c) სრულად უნდა იყოს განსაზღვრული, რადგან step მნიშვნელოვნად ცვლის შედეგს.

# 4️⃣ უარყოფითი step-ის გამოყენება
print("\nExample 4:")
for i in range(10, 0, -2):  # start = 10, stop = 0, step = -2
    print(i)  # 10, 8, 6, 4, 2
