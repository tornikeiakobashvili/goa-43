"""1) ჯერ for ციკლის საშვალებით გამოიტანეთ რიცხვები 5-იდან 15-მდე. შემდეგ კი while ციკლით, კომენატრებით ახსენით რა განსხვავებაა ამ 2 ციკლს შორის და რომელი ჯობია ამ კონკრეტულ შემთხვევაში"""
for i in range (5,15):
    print(i)

    
num = 5
while num < 15:
    print(num)
    num += 1

