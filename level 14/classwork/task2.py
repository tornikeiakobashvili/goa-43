"""2) გააკეთეთ 2-2 მაგალითი თთოეულ შედარების ოპერატორზე, გვერდზე კომენტარის საშვალებით მიუთითეთ თუ რომელ boolean მნიშვნელობას გამოიტანას, ასევე კომენტარებით ახსენით თუ რა არის boolean"""
print(10 > 9) # -> true
print(7 > 40) # -> false
 
print(7 <= 10) # -> true
print(7 >= 10) # -> false

print(10 == 10) # -> true
print(10 == 12) # -> false
 
print(5 != 5) # -> false
print(5 != 6) # -> true

